package nohorjo.centsa.vo;

public class TransactionsSummary {
    private int sum;
    private int count;

    public void setSum(int sum) {
        this.sum = sum;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getSum() {
        return sum;
    }

    public int getCount() {
        return count;
    }
}
